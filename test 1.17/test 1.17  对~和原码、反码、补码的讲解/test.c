#include<stdio.h>

int main()
{
	int a = 0;//4个字节=32个比特位  所以有32个0
	int b = ~a;//b有32个1
	//~--以(二进制)取反 -- 1010->0101
	printf("%d\n", b);//打印出来的b是这个数的原码
	return 0;
}
//负数在内存中储存的时候，储存的是二进制的补码
//原码、反码、补码
//11111111111111111111111111111111  补码
//11111111111111111111111111111110  反码
//10000000000000000000000000000001  原码
//开头的/最高位的数决定正负：1为+，0为-，开头的数在转换过程中不变
//原码取反->反码，反码+1->补码