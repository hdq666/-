#include<stdio.h>

int main()//讲解在鹏哥视频的p22 18.的104分左右
{
	char a = 3;//用的是char，所以只有8个比特位
	//00000011
	char b = 127;
	//‭01111111‬
	char c = a + b;
	//10000010
	//补充时按最高位补充
	//11111111111111111111111110000010//补码//这是整形提升
	//11111111111111111111111110000001//反码
	//10000000000000000000000001111110//源码
	printf("%d ", c);//%d打印出的是整形,所以用源码
	return 0;
}